# \t 在控制台输出一个 支付表，协助在输出文本时，垂直方向 保持对齐
# \n 在控制台输出一个 换行符
# \' 在字符前输出一个'
# \" 在字符前输出一个"
# \\ 在字符前输出一个\
# \r 在字符间进行回车,如果前面有字符，前面的字符便不会显示
# \b 会把前一个字符消失
# 其他的查看https://blog.csdn.net/wugsh15/article/details/51492085
print("1,\t2,\t3")
print("10,\t20,\t30")
print("hello \npyhton")
print("hello\'python")
print("hello\"python")
print("hello\\python")
print("hello\rpython")
print("hello\bpython")
